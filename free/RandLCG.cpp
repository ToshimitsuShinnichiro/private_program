#include "RandLCG.h"

//==============================================================================
//	関数名	RandLCG
//	引数	None
//	戻り値	None
//==============================================================================
CRandLCG::CRandLCG()
{
	a = 1664525;
	b = 1013904223;
	seed = 1234567890;
}

//==============================================================================
//	関数名	~RandLCG
//	引数	None
//	戻り値	None
//==============================================================================
CRandLCG::~CRandLCG()
{
}

//==============================================================================
//	関数名	getRand
//	引数	int, int
//	戻り値	None
//==============================================================================
int CRandLCG::getRand( int min, int max )
{
	seed = a * seed + b;

	return SCast<int>( seed % ( max - min + 1 ) + min );
}

